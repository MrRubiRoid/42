#include "libft.h"
#include <stdlib.h>

void	ft_putnbr_fd(int n, int fd)
{
	char	*num;

	num = ft_itoa(n);
	ft_putstr_fd(num, fd);
	free(num);
}

int	main(void)
{
	int n = -2147483647;
	ft_putnbr_fd(n, 1);
	return (0);
}